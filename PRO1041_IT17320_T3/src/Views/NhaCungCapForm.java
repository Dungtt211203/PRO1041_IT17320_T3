    public void getData() {
        DefaultTableModel dtm = (DefaultTableModel) this.tbNCC.getModel();
        dtm.setRowCount(0);
        for (NhaCungCapVM m : this.nccService.getAll()) {
            Object[] rowData = {
                m.getId(), m.getMa(), m.getTen()
            };
            dtm.addRow(rowData);
        }
    }

