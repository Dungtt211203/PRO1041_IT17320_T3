    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        NhaCungCap ncc = this.getLoadData();
        if (ncc == null) {
            return;
        }

        this.nccService.insert(ncc);
        this.getData();

        JOptionPane.showMessageDialog(this, "Thêm thành công");
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        int row = tbNCC.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Hãy chọn dòng cần sửa");
        }
        NhaCungCap ncc = this.getLoadData();
        if (ncc == null) {
            return;
        }
        this.nccService.update(ncc, ncc.getId());
        this.getData();

        JOptionPane.showMessageDialog(this, "Sửa thành công");
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int row = tbNCC.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Hãy chọn dòng cần xóa");
        } else {
            int confirm = JOptionPane.showConfirmDialog(this, "Xác nhận xóa??");
            if (confirm == JOptionPane.YES_NO_OPTION) {
                String id = tbNCC.getValueAt(row, 0).toString();
                this.nccService.delete(id);
                this.getData();

            }
        }
        JOptionPane.showMessageDialog(this, "Xóa thành công");
    }//GEN-LAST:event_btnXoaActionPerformed

    private void tbNCCMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbNCCMouseClicked
        // TODO add your handling code here:
        int row = tbNCC.getSelectedRow();
        lbId.setText(tbNCC.getValueAt(row, 0).toString());
        txtMa.setText(tbNCC.getValueAt(row, 1).toString());
        txtTen.setText(tbNCC.getValueAt(row, 2).toString());
    }//GEN-LAST:event_tbNCCMouseClicked

    /**
     * @param args the command line arguments
     */
    private NhaCungCap getLoadData() {
        String id = this.lbId.getText();
        String ma = this.txtMa.getText().trim();
        String ten = this.txtTen.getText().trim();
        if (ma.equals("") || ten.equals("")) {
            JOptionPane.showMessageDialog(this, "Không được để trống!!!");
            return null;
        }
        NhaCungCap ncc = new NhaCungCap(id, ma, ten);
        return ncc;
    }

