    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        ChiTietSP cs=this.getLoadData();
        if(cs==null){
            return;
        }

        this.csService.insert(cs);
        this.getData();
        this.clear();
        JOptionPane.showMessageDialog(this, "Thêm thành công");
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        int row=tbCTSP.getSelectedRow();
        if(row==-1){
            JOptionPane.showMessageDialog(this, "Hãy chọn dòng cần sửa");
        }
        ChiTietSP cs=this.getLoadData();
        if(cs==null){
            return;
        }
        this.csService.update(cs, cs.getId());
        this.getData();
        this.clear();
        JOptionPane.showMessageDialog(this, "Sửa thành công");
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int row=tbCTSP.getSelectedRow();
        if(row==-1){
            JOptionPane.showMessageDialog(this, "Hãy chọn dòng cần xóa");
        }else{
            int confirm=JOptionPane.showConfirmDialog(this, "Xác nhận xóa??");
            if(confirm==JOptionPane.YES_NO_OPTION){
                String id=tbCTSP.getValueAt(row, 0).toString();
                this.csService.delete(id);
                this.getData();
                this.clear();
            }
        }
        JOptionPane.showMessageDialog(this, "Xóa thành công");
    }//GEN-LAST:event_btnXoaActionPerformed

    private void tbCTSPMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbCTSPMouseClicked
        // TODO add your handling code here:
        int row=tbCTSP.getSelectedRow();
        lblID.setText(tbCTSP.getValueAt(row, 0).toString());
        txtMoTa.setText(tbCTSP.getValueAt(row, 1).toString());
        txtSL.setText(tbCTSP.getValueAt(row, 2).toString());
        txtGiaNhap.setText(tbCTSP.getValueAt(row, 3).toString());
        txtGiaBan.setText(tbCTSP.getValueAt(row, 4).toString());
    }//GEN-LAST:event_tbCTSPMouseClicked

    
        private ChiTietSP getLoadData(){
        String id=this.lblID.getText();
        String mt=this.txtMoTa.getText().trim();
        String sl=this.txtSL.getText().trim();
        String gn=this.txtGiaNhap.getText().trim();
        String gb=this.txtGiaBan.getText().trim();
        
        
        int soLuong=Integer.parseInt(sl);
        double giaNhap=Double.parseDouble(gn);
        double giaBan=Double.parseDouble(gb);
        ChiTietSP cs=new ChiTietSP(id, mt, soLuong, giaNhap, giaBan);
        return cs;
    }
    
    private void clear(){
        lblID.setText("");
        txtMoTa.setText("");
        txtSL.setText("");
        txtGiaNhap.setText("");
        txtGiaBan.setText("");
    }
