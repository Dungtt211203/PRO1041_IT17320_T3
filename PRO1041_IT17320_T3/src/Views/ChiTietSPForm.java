    public void getData(){
        DefaultTableModel dtm=(DefaultTableModel) this.tbCTSP.getModel();
        dtm.setRowCount(0);
        for (ChiTietSPViewModel cs : this.csService.getAll()) {
            Object[] rowData={
                cs.getId(),cs.getMoTa(),cs.getSoLuong(),cs.getGiaNhap(),cs.getGiaBan()
            };
            dtm.addRow(rowData);
        }
    }
